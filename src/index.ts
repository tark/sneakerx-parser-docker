const createProduct = async (originalProducts: OriginalProduct[], p: OriginalProduct) => {

  if (!p.name) {
    console.log(`createProduct - product is undefined, skip`)
    return
  }

  if (originalProducts.find(op => op.name === p.name && op.color === p.color)) {
    console.log(`createProduct - product "${p.name}"/"${p.color}" already exists, skip`)
    return
  }

  const photosUrls: string[] = []

  console.log(`createProduct - uploading photos...`)

  await Promise.all(p.photos?.map(async (url, index) => {
    const blob = await downloadImage(url ?? '')
    const newUrl = await uploadFile(blob, `${p.name}_${index}.png`)
    photosUrls.push(newUrl)
  }) ?? [])

  console.log(`createProduct - creating product...`)

  photosUrls.sort()

  await createOriginalProduct({
    ...p,
    photos: photosUrls
  })

  console.log(`createProduct - product created - ${p.name}`)
}

